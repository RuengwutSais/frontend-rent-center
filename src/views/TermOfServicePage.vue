    <h1>8.ประเภทบุคคลที่ ศออ. เปิดเผยข้อมูลส่วนบุคคลของท่าน</h1>
    <p>
      ภายใต้วัตถุประสงค์ที่ได้ระบุไว้ในข้อ 7 ข้างต้น ศออ.
      อาจเปิดเผยข้อมูลส่วนบุคคลของท่านให้แก่บุคคลดังต่อไปนี้ ทั้งนี้
      ประเภทของบุคคลผู้รับข้อมูลที่ระบุไว้ดังต่อไปนี้เป็นเพียงกรอบการเปิดเผยข้อมูลส่วนบุคคลของ
      ศออ. เป็นการทั่วไป
      เฉพาะบุคคลผู้รับข้อมูลที่เกี่ยวข้องกับบริการที่ท่านใช้งานหรือมีความสัมพันธ์ด้วยเท่านั้นที่จะมีผลบังคับใช้
    </p>
    <p>
      1) หน่วยงานของรัฐหรือผู้มีอำนาจที่ ศออ.
      ต้องเปิดเผยข้อมูลเพื่อวัตถุประสงค์ในการดำเนินการตามกฎหมายหรือวัตถุประสงค์สำคัญอื่น
      (เช่น การดำเนินการเพื่อประโยชน์สาธารณะ) หน่วยงานผู้บังคับใช้กฎหมาย
      หรือมีอำนาจควบคุมกำกับดูแลหรือมีวัตถุประสงค์อื่นที่มีความสำคัญ เช่น
      คณะรัฐมนตรี รัฐมนตรีผู้รักษาการ กรมการปกครอง กรมสรรพากร สำนักงานตำรวจ ศาล
      สำนักงานอัยการ กรมควบคุมโรค กระทรวงดิจิทัลเพื่อเศรษฐกิจและสังคม
      สำนักงานปลัดสำนักนายก กรมการกงสุล กองทุนเงินให้กู้ยืมเพื่อการศึกษา เป็นต้น
    </p>
    <p>
      2) ผู้ใช้บริการทั่วไป ที่มีการเข้ามาใช้บริการของ ศออ. เช่น
      ผู้ประสงค์เช่าอสังหาริมทรัพย์ ผู้ประสงค์ปล่อยเช่าอสังหาริมทรัพย์
    </p>
    <p>
      3) การเปิดเผยข้อมูลต่อสาธารณะ ศออ.
      อาจเปิดเผยข้อมูลของท่านต่อสาธารณะในกรณีที่จำเป็น เช่น
      การดำเนินการที่กำหนดให้ ศออ.
      ต้องประกาศลงในราชกิจจานุเบกษาหรือมติคณะรัฐมนตรี เป็นต้น
    </p>
    <h1>9.ระยะเวลาในการเก็บรวบรวมข้อมูลส่วนบุคคลของท่าน</h1>
    <p>
      ศออ.
      จะเก็บรักษาข้อมูลส่วนบุคคลของท่านไว้ในระยะเวลาเท่าที่ข้อมูลนั้นยังมีความจำเป็นตามวัตถุประสงค์ในการเก็บรวบรวมข้อมูลหรือตามระยะเวลาที่ท่านประสงค์ท่านั้นเท่านั้น
      ตามรายละเอียดที่ได้กำหนดไว้ในนโยบาย ประกาศหรือตามกฎหมายที่เกี่ยวข้อง
      ทั้งนี้
      เมื่อพ้นระยะเวลาและข้อมูลส่วนบุคคลของท่านสิ้นความจำเป็นตามวัตถุประสงค์ดังกล่าวแล้ว
      ศออ. จะทำการลบ ทำลายข้อมูลส่วนบุคคลของท่าน
      หรือทำให้ข้อมูลส่วนบุคคลของท่านไม่สามารถระบุตัวตนได้ต่อไป
      ตามรูปแบบและมาตรฐานการลบทำลายข้อมูลส่วนบุคคลที่คณะกรรมการหรือกฎหมายจะได้ประกาศกำหนดหรือตามมาตรฐานสากล
      อย่างไรก็ดี ในกรณีที่มีข้อพิพาท
      การใช้สิทธิหรือคดีความอันเกี่ยวข้องกับข้อมูลส่วนบุคคลของท่าน ศออ.
      ขอสงวนสิทธิในการเก็บรักษาข้อมูลนั้นต่อไปจนกว่าข้อพิพาทนั้นจะได้มีคำสั่งหรือคำพิพากษาถึงที่สุด
    </p>
    <h1>10.สิทธิของท่านตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562</h1>
    <p>
      พระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562
      ได้กำหนดสิทธิของเจ้าของข้อมูลส่วนบุคคลไว้หลายประการ ทั้งนี้
      สิทธิดังกล่าวจะเริ่มมีผลบังคับใช้เมื่อกฎหมายในส่วนของสิทธินี้มีผลใช้บังคับ
      โดยรายละเอียดของสิทธิต่าง ๆ ประกอบด้วย
    </p>
    <p>
      1) สิทธิในการขอเข้าถึงข้อมูลส่วนบุคคล ท่านมีสิทธิขอเข้าถึง
      รับสำเนาและขอให้เปิดเผยที่มาของข้อมูลส่วนบุคคลที่ ศออ.
      เก็บรวบรวมไว้โดยปราศจากความยินยอมของท่าน เว้นแต่กรณีที่ ศออ.
      มีสิทธิปฏิเสธคำขอของท่านด้วยเหตุตามกฎหมายหรือคำสั่งศาล
      หรือกรณีที่การใช้สิทธิของท่านจะมีผลกระทบที่อาจก่อให้เกิดความเสียหายต่อสิทธิและเสรีภาพของบุคคลอื่น
    </p>
    <p>
      2) สิทธิในการขอแก้ไขข้อมูลส่วนบุคคลให้ถูกต้อง สมบูรณ์และเป็นปัจจุบัน
      หากท่านพบว่าข้อมูลส่วนบุคคลของท่านไม่ถูกต้อง ไม่ครบถ้วนหรือไม่เป็นปัจจุบัน
      ท่านมีสิทธิขอให้แก้ไขเพื่อให้มีความถูกต้อง เป็นปัจจุบัน สมบูรณ์
      และไม่ก่อให้เกิดความเข้าใจผิดได้
    </p>
    <p>
