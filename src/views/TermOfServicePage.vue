      หรือผู้อนุบาล หรือผู้พิทักษ์ตามแต่กรณี ดังนี้ ศออ.
      จะดำเนินการลบทำลายข้อมูลส่วนบุคคลนั้นโดยเร็วหาก ศออ.
      ไม่มีเหตุอันชอบด้วยกฎหมายประการอื่นนอกเหนือจากความยินยอมในการเก็บรวบรวม
      ใช้หรือเปิดเผยข้อมูลดังกล่าว
    </p>
    <h1>7.วัตถุประสงค์ในการเก็บรวบรวมข้อมูลส่วนบุคคล</h1>
    <p>
      ศออ. ดำเนินการเก็บรวบรวมข้อมูลส่วนบุคคลของท่านเพื่อวัตถุประสงค์หลายประการ
      ซึ่งขึ้นอยู่กับประเภทของบริการหรือกิจกรรมที่ท่านใช้บริการ
      ตลอดจนลักษณะความสัมพันธ์ของท่านกับ ศออ.
      หรือข้อพิจารณาในแต่ละบริบทเป็นสำคัญ
      โดยวัตถุประสงค์ที่ระบุไว้ดังต่อไปนี้เป็นเพียงกรอบการใช้ข้อมูลส่วนบุคคลของ
      ศออ. เป็นการทั่วไป ทั้งนี้
      เฉพาะวัตถุประสงค์ที่เกี่ยวข้องกับผลิตภัณฑ์หรือบริการที่ท่านใช้งานหรือมีความสัมพันธ์ด้วยเท่านั้นที่จะมีผลบังคับใช้กับข้อมูลของท่าน
    </p>
    <p>
      1) เพื่อให้บริการและบริหารจัดการบริการของ ศออ.
      ทั้งบริการภายใต้สัญญาที่มีต่อท่าน หรือตามพันธกิจของ ศออ.
    </p>
    <p>2) เพื่อการดำเนินการทางธุรกรรมของ ศออ.</p>
    <p>
      3) ควบคุมดูแล ใช้งาน ติดตาม
      ตรวจสอบและบริหารจัดการบริการเพื่ออำนวยความสะดวกและสอดคล้องกับความต้องการของท่าน
    </p>
    <p>
      4) เพื่อเก็บรักษาและปรับปรุงข้อมูลอันเกี่ยวกับท่าน
      รวมทั้งเอกสารที่มีการกล่าวอ้างถึงท่าน
    </p>
    <p>5) จัดทำบันทึกรายการการประมวลผลข้อมูลส่วนบุคคลตามที่กฎหมายกำหนด</p>
    <p>6) วิเคราะห์ข้อมูล รวมถึงแก้ไขปัญหาที่เกี่ยวกับบริการของ ศออ.</p>
    <p>
      7) เพื่อดำเนินการตามที่จำเป็นในการบริหารจัดการภายในองค์กร
      รวมถึงการรับสมัครงาน การสรรหากรรมการหรือผู้ดำรงตำแหน่งต่าง ๆ
      การประเมินคุณสมบัติ
    </p>
    <p>
      8) ป้องกัน ตรวจจับ หลีกเลี่ยง และตรวจสอบการฉ้อโกง การละเมิดความปลอดภัย
      หรือการกระทำที่ต้องห้าม หรือผิดกฎหมาย และอาจเกิดความเสียหายต่อทั้ง ศออ.
      และเจ้าของข้อมูลส่วนบุคคล
    </p>
    <p>
      9) การยืนยันตัวตน พิสูจน์ตัวตนและตรวจสอบข้อมูลเมื่อท่านสมัครใช้บริการของ
      ศออ. หรือติดต่อใช้บริการ หรือใช้สิทธิตามกฎหมาย
    </p>
    <p>10) ปรับปรุงและพัฒนาคุณภาพบริการให้ทันสมัย</p>
    <p>11) การประเมินและบริหารจัดการความเสี่ยง</p>
    <p>
      12) ส่งการแจ้งเตือน การยืนยันการทำคำสั่ง
      ติดต่อสื่อสารและแจ้งข่าวสารไปยังท่าน
    </p>
    <p>
      13) ยืนยันตัวตน ป้องกันการสแปม หรือการกระทำที่ไม่ได้รับอนุญาต
      หรือผิดกฎหมาย
    </p>
    <p>
      14) ดำเนินการตามที่จำเป็นเพื่อปฏิบัติตามหน้าที่ที่ ศออ.
      มีต่อหน่วยงานที่มีอำนาจควบคุม หน่วยงานด้านภาษี การบังคับใช้กฎหมาย
      หรือภาระผูกพันตามกฎหมายของ ศออ.
