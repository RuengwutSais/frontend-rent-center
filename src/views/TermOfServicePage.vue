      เช่น ขนาดอสังหาริมทรัพย์ แผนที่ตั้งของอสังหาริมทรัพย์
      ประเภทของอสังหาริมทรัพย์ รหัสไปรษณีย์ จำนวนห้องของอสังหาริมทรัพย์ เป็นต้น
